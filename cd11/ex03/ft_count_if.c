int	ft_count_if(char **tab, int length, int(*f)(char*))
{
	int	i;
	int	ans;

	i = 0;
	ans = 0;
	while (i < length)
	{
		if ((*f)(tab[i]))
			ans++;
		i++;
	}
	return (ans);
}
