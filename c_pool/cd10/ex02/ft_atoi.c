int	ft_atoi(char *str)
{
	int		i;
	int		ans;

	ans = 0;
	i = 0;
	while ((*(str + i) >= '0') && (*(str + i) <= '9'))
	{
		ans = (ans * 10) + (*(str + i) - 48);
		i++;
	}
	return (ans);
}
